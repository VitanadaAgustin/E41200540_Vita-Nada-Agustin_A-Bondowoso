class Person {
    String namadepan , namapanjang , nim , status;

    public Person(String namadepan, String namapanjang, String nim, String status) {
        this.namadepan = namadepan;
        this.namapanjang = namapanjang;
        this.nim = nim;
        this.status = status;
    }

    public String getNamadepan() {
        return namadepan;
    }

    public String getNamapanjang() {
        return namapanjang;
    }

    public String getNim() {
        return nim;
    }

    public String getStatus() {
        return status;
    }

    
    
}
  